package com.minersleague.main.permissions;

public class Group {

	private String name;
	private String color;
	
	public Group() {}
	
	public Group(String name, String color) {
		this.name = name;
		this.color = color;
	}
	
	public String getName() {
		return name;
	}
	
	public String getColor() {
		return color;
	}
	
}
